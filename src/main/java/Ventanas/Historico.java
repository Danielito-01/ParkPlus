    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
    if (jdcDesde.getDate() == null || jdcHasta.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Seleccione ambas fechas.");
            return;
        }

        LocalDate desde = jdcDesde.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate hasta = jdcHasta.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

        // Validación
        if (hasta.isBefore(desde)) {
            JOptionPane.showMessageDialog(this, "La fecha 'hasta' no puede ser menor que la fecha 'desde'.");
            return;
        }
        ReporteDAO dao = new ReporteDAO();
        List<Ticket> lista = dao.obtenerTicketsCerradosPorRango(desde, hasta);
        cargarHistoricoEnTabla(lista);
    }//GEN-LAST:event_btnConsultarActionPerformed

