private void cargarHistoricoEnTabla(List<Ticket> lista) {

    DefaultTableModel model = (DefaultTableModel) tblHistorico.getModel();
    model.setRowCount(0);

    for (Ticket t : lista) {
        model.addRow(new Object[]{                     
            t.getPlacaVehiculo(),           
            t.getCarnetUsuario(),           
            t.getTipoUsuario(),             
            t.getTipoVehiculo(),            
            t.getCodigoSpot(),              
            t.getCodigoArea(),              
            t.getFechaHoraIngreso(),        
            t.getFechaHoraSalida(),         
            t.getTarifaAplicada(),          
            "Q" + t.getMonto(),             
            t.getMetodoPago(), 
        });
    }
}
    
